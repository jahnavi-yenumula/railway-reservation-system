-- STEP 5: CREATING SCHEMATIC VIEWS FOR APPLICATION INTERFACES
-- Refinement 2 View: Calculates distance dynamically and checks against structural fares.
CREATE OR REPLACE VIEW v_booking_fare_estimates AS
SELECT 
    b.pnr_10,
    b.train_no,
    b.source_stn_code,
    b.destination_stn_code,
    r_src.distance_from_source AS src_km,
    r_dest.distance_from_source AS dest_km,
    (r_dest.distance_from_source - r_src.distance_from_source) AS distance_km,
    cc.class_code,
    cc.class_name,
    cc.base_fare_multiplier,
    -- Dynamic Fare Calculation formula:
    ((r_dest.distance_from_source - r_src.distance_from_source) * cc.base_fare_multiplier) AS dynamic_estimated_fare
FROM bookings b
JOIN routes r_src ON b.train_no = r_src.train_no AND b.source_stn_code = r_src.stn_code
JOIN routes r_dest ON b.train_no = r_dest.train_no AND b.destination_stn_code = r_dest.stn_code
JOIN tickets t ON b.pnr_10 = t.pnr_10
LEFT JOIN train_composition tc ON t.composition_id = tc.composition_id
LEFT JOIN coach_classes cc ON tc.class_code = cc.class_code;

