-- STEP 2: CREATE CORE INFRASTRUCTURE TABLES

CREATE TABLE stations (
    stn_code VARCHAR2(10) PRIMARY KEY,
    city VARCHAR2(100) NOT NULL,
    stn_name VARCHAR2(255) NOT NULL,
    state VARCHAR2(100) NOT NULL,
    CONSTRAINT chk_stn_code_upper CHECK (stn_code = UPPER(stn_code))
);

CREATE TABLE trains (
    train_no VARCHAR2(10) PRIMARY KEY,
    train_name VARCHAR2(255) NOT NULL,
    train_type VARCHAR2(50) NOT NULL,
    active_days CHAR(7) DEFAULT '1111111' NOT NULL, -- Binary schedule string (MTWTFSS)
    CONSTRAINT chk_train_no_numeric CHECK (REGEXP_LIKE(train_no, '^[0-9]+$')),
    CONSTRAINT chk_train_type CHECK (train_type IN ('Express', 'Superfast', 'Rajdhani', 'Shatabdi', 'Passenger', 'Duronto'))
);

-- Refinement 3: Time Tracking optimized to absolute numerical offsets from midnight (0 to 1439 minutes)
-- This eliminates sorting issues and simplifies multi-day scheduling computations.
CREATE TABLE routes (
    route_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    train_no VARCHAR2(10) NOT NULL REFERENCES trains(train_no) ON DELETE CASCADE,
    stn_code VARCHAR2(10) NOT NULL REFERENCES stations(stn_code),
    arrival_time NUMBER(4),  -- Minutes since midnight (e.g. 08:05 is 485)
    depart_time NUMBER(4),   -- Minutes since midnight (e.g. 16:50 is 1010)
    sequence_num NUMBER(3) NOT NULL,
    distance_from_source NUMBER(5) DEFAULT 0 NOT NULL,
    CONSTRAINT chk_seq_num CHECK (sequence_num > 0),
    CONSTRAINT chk_distance CHECK (distance_from_source >= 0),
    CONSTRAINT chk_arr_time CHECK (arrival_time IS NULL OR (arrival_time BETWEEN 0 AND 1439)),
    CONSTRAINT chk_dep_time CHECK (depart_time IS NULL OR (depart_time BETWEEN 0 AND 1439)),
    CONSTRAINT uq_train_stn UNIQUE (train_no, stn_code),
    CONSTRAINT uq_train_seq UNIQUE (train_no, sequence_num)
);

CREATE TABLE train_instances (
    train_no VARCHAR2(10) NOT NULL REFERENCES trains(train_no),
    run_date DATE NOT NULL,
    actual_departure TIMESTAMP,
    actual_arrival TIMESTAMP,
    status VARCHAR2(20) DEFAULT 'Scheduled' NOT NULL,
    delay_minutes NUMBER(4) DEFAULT 0 NOT NULL,
    CONSTRAINT pk_train_instances PRIMARY KEY (train_no, run_date),
    CONSTRAINT chk_instance_status CHECK (status IN ('Scheduled', 'Running', 'Delayed', 'Cancelled', 'Completed'))
);

-- Refinement 1A: Coach Classes Master Catalog defining static configurations
CREATE TABLE coach_classes (
    class_code VARCHAR2(10) PRIMARY KEY, -- E.g., '1A', '2A', '3A', 'SL', 'CC', '2S'
    class_name VARCHAR2(100) NOT NULL,    -- E.g., 'AC First Class', 'AC 3-Tier', 'Sleeper Class'
    total_seats NUMBER(3) NOT NULL,       -- Total operational capacity per coach
    base_fare_multiplier NUMBER(4, 2) NOT NULL, -- Fare factor applied per kilometer (e.g., 2.45)
    CONSTRAINT chk_total_seats CHECK (total_seats > 0),
    CONSTRAINT chk_multiplier CHECK (base_fare_multiplier > 0.00)
);

-- Refinement 1B: Train Composition Table (Junction linking dynamic run configurations)
-- Provides flexibility to add/remove specific coaches on any given scheduled operational date.
CREATE TABLE train_composition (
    composition_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    train_no VARCHAR2(10) NOT NULL,
    run_date DATE NOT NULL,
    coach_label VARCHAR2(10) NOT NULL, -- E.g., 'B1', 'B2', 'H1'
    class_code VARCHAR2(10) NOT NULL REFERENCES coach_classes(class_code),
    CONSTRAINT fk_comp_train_instance FOREIGN KEY (train_no, run_date) REFERENCES train_instances(train_no, run_date) ON DELETE CASCADE,
    CONSTRAINT uq_train_run_coach UNIQUE (train_no, run_date, coach_label)
);

-- STEP 3: USER & TRANSACTION INFRASTRUCTURE

CREATE TABLE users (
    user_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email VARCHAR2(255) UNIQUE NOT NULL,
    mobile VARCHAR2(15) UNIQUE NOT NULL,
    first_name VARCHAR2(100) NOT NULL,
    last_name VARCHAR2(100) NOT NULL,
    dob DATE NOT NULL,
    gender VARCHAR2(10) NOT NULL,
    -- Refinement 3: Restricting password_hash to exact length of standard bcrypt format (60 chars)
    password_hash CHAR(60) NOT NULL,
    role VARCHAR2(20) DEFAULT 'Passenger' NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT chk_user_email CHECK (email LIKE '%@%.%'),
    CONSTRAINT chk_user_mobile CHECK (REGEXP_LIKE(mobile, '^[0-9]{10}$')),
    CONSTRAINT chk_user_gender CHECK (gender IN ('Male', 'Female', 'Other')),
    CONSTRAINT chk_user_role CHECK (role IN ('Admin', 'Station Master', 'Passenger', 'TC'))
);

-- Refinement 2: Dynamic Stop-Specific Fare Columns added (source_stn_code, destination_stn_code)
CREATE TABLE bookings (
    pnr_10 VARCHAR2(10) PRIMARY KEY,
    user_id NUMBER NOT NULL REFERENCES users(user_id),
    train_no VARCHAR2(10) NOT NULL,
    journey_date DATE NOT NULL,
    source_stn_code VARCHAR2(10) NOT NULL REFERENCES stations(stn_code),
    destination_stn_code VARCHAR2(10) NOT NULL REFERENCES stations(stn_code),
    quota VARCHAR2(5) DEFAULT 'GN' NOT NULL, 
    total_fare NUMBER(10, 2) NOT NULL,
    booking_timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    status VARCHAR2(20) DEFAULT 'Booked' NOT NULL,
    CONSTRAINT fk_booking_train_instance FOREIGN KEY (train_no, journey_date) REFERENCES train_instances(train_no, run_date),
    CONSTRAINT chk_pnr_format CHECK (REGEXP_LIKE(pnr_10, '^[0-9]{10}$')),
    CONSTRAINT chk_booking_fare CHECK (total_fare >= 0),
    CONSTRAINT chk_booking_status CHECK (status IN ('Booked', 'Cancelled', 'Partially_Cancelled')),
    CONSTRAINT chk_quota CHECK (quota IN ('GN', 'TQ', 'LD', 'DF', 'SR')),
    CONSTRAINT chk_different_stns CHECK (source_stn_code <> destination_stn_code)
);

CREATE TABLE tickets (
    ticket_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pnr_10 VARCHAR2(10) NOT NULL REFERENCES bookings(pnr_10) ON DELETE CASCADE,
    passenger_name VARCHAR2(150) NOT NULL,
    passenger_age NUMBER(3) NOT NULL,
    passenger_gender VARCHAR2(10) NOT NULL,
    -- Tickets reference the dynamic composition layout rather than a static catalog row
    composition_id NUMBER REFERENCES train_composition(composition_id),
    seat_num NUMBER(3),
    allocated_berth VARCHAR2(5), 
    -- Refinement 3: Strict Status constraint checks to avoid dirty entries
    status VARCHAR2(5) NOT NULL, 
    wl_number NUMBER(3) DEFAULT 0,
    CONSTRAINT chk_pass_age CHECK (passenger_age BETWEEN 0 AND 125),
    CONSTRAINT chk_pass_gender CHECK (passenger_gender IN ('Male', 'Female', 'Other')),
    CONSTRAINT chk_ticket_status CHECK (status IN ('CNF', 'RAC', 'WL', 'CAN')),
    CONSTRAINT chk_berth_type CHECK (allocated_berth IS NULL OR allocated_berth IN ('LB', 'MB', 'UB', 'SL', 'SU', 'WS'))
);

CREATE TABLE payments (
    trans_id VARCHAR2(100) PRIMARY KEY,
    pnr_10 VARCHAR2(10) NOT NULL REFERENCES bookings(pnr_10),
    amount NUMBER(10, 2) NOT NULL,
    payment_status VARCHAR2(20) DEFAULT 'Success' NOT NULL,
    payment_method VARCHAR2(50) NOT NULL,
    paid_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT chk_pay_amount CHECK (amount >= 0),
    CONSTRAINT chk_pay_status CHECK (payment_status IN ('Success', 'Failed', 'Refunded', 'Pending')),
    CONSTRAINT chk_pay_method CHECK (payment_method IN ('UPI', 'Credit Card', 'Debit Card', 'Net Banking', 'Wallet'))
);

CREATE TABLE master_passengers (
    mstr_pass_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id NUMBER NOT NULL REFERENCES users(user_id) ON DELETE CASCADE,
    name VARCHAR2(150) NOT NULL,
    age NUMBER(3) NOT NULL,
    gender VARCHAR2(10) NOT NULL,
    preferred_berth VARCHAR2(5),
    CONSTRAINT chk_mstr_age CHECK (age BETWEEN 0 AND 125),
    CONSTRAINT chk_mstr_gender CHECK (gender IN ('Male', 'Female', 'Other')),
    CONSTRAINT chk_mstr_berth CHECK (preferred_berth IS NULL OR preferred_berth IN ('LB', 'MB', 'UB', 'SL', 'SU', 'WS'))
);

